export type TranslationKey = keyof typeof translations.de;

export function t(locale: UILocale, key: TranslationKey): string {
  return translations[locale]?.[key] ?? translations.de[key];
}

export function getStoredLocale(): UILocale {
  if (typeof window === "undefined") return "de";
  const stored = localStorage.getItem("pincraft_ui_locale");
  if (stored === "de" || stored === "ru" || stored === "en") return stored;
  return "de";
}

export function setStoredLocale(locale: UILocale): void {
  localStorage.setItem("pincraft_ui_locale", locale);
  window.dispatchEvent(new Event("storage"));
}
